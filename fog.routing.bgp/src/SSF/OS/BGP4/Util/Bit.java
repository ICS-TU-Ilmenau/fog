package SSF.OS.BGP4.Util;


// ===== class SSF.OS.BGP4.Util.Bit ======================================== //
/**
 * Contains two very simple constants describing the possible values of a
 * single bit.
 */
public class Bit {

  // ......................... constants ........................... //

  /** Indicates a bit value of 0.  Defined simply to make dealing with booleans
   *  as bits a little easier. */
  public static final boolean zero = false;

  /** Indicates a bit value of 1.  Defined simply to make dealing with booleans
   *  as bits a little easier. */
  public static final boolean one  = true;

} // end of class Bit
